// general inquery
$(document).ready(function () {
    $('#loan_1').click(function (e) {
        $('#sign_up').lightbox_me({
            centered: true,
            onLoad: function () {
                $('#sign_up').find('input:first').focus()
            }
        });
        e.preventDefault();
    });

    $ele.lightbox_me();
    $ele.trigger('close');
});
